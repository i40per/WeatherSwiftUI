                            }
                        }
                }
            } else {
                if locationManager.isLoading {
                    LoadingView()
                } else {
                    WelcomeView()
                        .environmentObject(locationManager)
                }
            }
        }
        .background(Color(red: 0.205, green: 0.264, blue: 0.289))
        .preferredColorScheme(.dark)
    }
}

